CREATE TABLE AD_TEACHER (
    id NUMBER(8),
    first_name VARCHAR2(30),
    last_name VARCHAR2(30),
    email VARCHAR2(40),
    salary NUMBER(10),
    hire_date DATE,
    bonus NUMBER(10),
    dept_id NUMBER(6),
    PRIMARY KEY(id)
);
 
CREATE TABLE AD_TEACHER_COURSES (
    id NUMBER(6),
    name VARCHAR2(40),
    session_id NUMBER(4),
    dept_id NUMBER(6),
    building VARCHAR2(30),
    room NUMBER(6),
    timetable VARCHAR2(30),
    hours NUMBER(6),
    teacher_id NUMBER(6),
    PRIMARY KEY(id)
);

